"""Self-Learning Package ----------------------- این پکیج شامل کل سیستم خودآموزی مدل‌های زبانی است. این سیستم از چند
زیرماژول تشکیل شده که هر کدام وظایف خاصی در چرخه یادگیری مدل را بر عهده دارند:

  - base: اجزای پایه و زیرساخت‌های اصلی (مانند مدیریت پیکربندی، رویدادها، متریک‌ها، وضعیت، و ...).
  - need_detection: تشخیص نیازهای یادگیری (مانند تحلیل عملکرد، شکاف‌های دانشی، تشخیص روندها، تحلیل درخواست‌ها و بازخوردها).
  - acquisition: جمع‌آوری داده‌های آموزشی و هماهنگی ارتباط با ماژول‌های Balance و Data.
  - processing: پردازش داده‌های ورودی شامل تمیزسازی، ارزیابی کیفیت، تشخیص افزونگی و یکپارچه‌سازی دانش.
  - training: فرآیند آموزش مدل شامل مدیریت منابع آموزشی، زمان‌بندی تطبیقی، بهینه‌سازی دسته‌ها و تنظیم نرخ یادگیری.
  - strategy: استراتژی‌های آموزشی بر اساس فاز مدل (نوپا، در حال رشد، پخته) و کارخانه تولید استراتژی.
  - evaluation: ارزیابی عملکرد مدل شامل جمع‌آوری متریک‌های عملکرد، ارزیابی پوشش دانشی، کارایی یادگیری و پیگیری پیشرفت.
  - federation: آموزش توزیع‌شده و اشتراک دانش بین مدل‌های مختلف.
  - monitoring: نظارت بر فرآیندهای یادگیری و منابع مصرفی، ارسال هشدارها و گزارش‌دهی متریک‌ها.
  - config: تنظیمات پیش‌فرض و پارامترهای مقیاس‌بندی سیستم.

تمامی زیرماژول‌ها به صورت نهایی و عملیاتی پیاده‌سازی شده‌اند.
"""

